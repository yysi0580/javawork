package _02_print;

public class T01_print_println {

	public static void main(String[] args) {
		// TODO 출력문 : println -> 출력 후 줄바꿈
		//  문자열 출력은 반드시 쌍따옴표("") 안에 기재
		System.out.println("더조은 컴퓨터 학원");
		System.out.println("내 이름은 양시열");
		
		// 숫자일 때는 아무것도 붙이지 않는다.
		System.out.println(3+7);
		System.out.println(5*7);
		
		//print : 출력 후 줄바꿈 안함
		System.out.print("줄바꿈 안함");
		System.out.print("옆에 한 줄로 출력");
	}

}
