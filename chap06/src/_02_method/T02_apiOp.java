package _02_method;

public class T02_apiOp {
	int add(int num1,int num2) {
		return num1 + num2;
	}
	int minus(int num1,int num2) {
		return num1 - num2;
	}
	int multiply(int num1,int num2) {
		return num1 * num2;
	}
	double divide(double num1, double num2) {
		return num1 / num2;
	}
	int remainder(int num1,int num2) {
		return num1 % num2;
	}
}
